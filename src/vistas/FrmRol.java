    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if ((this.codRol.getText().length() == 0) || (this.textNombre.getText().length() == 0)) {
            Icon icon = null;
            JOptionPane.showMessageDialog(null, "ALGUN CAMPO VACIO", "ERROR", JOptionPane.DEFAULT_OPTION, icon);
        } else {
            String Nombre = this.textNombre.getText();
            int idRol = parseInt(this.codRol.getText());
            Icon icon = null;
 int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, icon);
            if (resp == JOptionPane.YES_OPTION) {
                ctrlRol a = new ctrlRol();
                a.insertarRol(idRol, Nombre);
                this.textNombre.setText(null);
                this.codRol.setText(null);
                agregar();
            }    
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

