    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if ((this.TextNombre.getText().length() == 0)
                || (this.codCancha.getText().length() == 0)
                || (this.estado.getText().length() == 0)) {
            Icon icon = null;
            JOptionPane.showMessageDialog(null, "ALGUN CAMPO VACIO", "ERROR", JOptionPane.DEFAULT_OPTION, icon);
        } else {
            int idCancha = parseInt(this.codCancha.getText());
            String nombre = this.TextNombre.getText();
            int estado = parseInt(this.estado.getText());
            Icon icon = null;
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, icon);
            if (resp == JOptionPane.YES_OPTION) {
                ctrlCancha a = new ctrlCancha();
                a.insertarCancha(idCancha, nombre, estado);
                this.TextNombre.setText(null);
                this.estado.setText(null);
                this.codCancha.setText(null);
                agregar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

