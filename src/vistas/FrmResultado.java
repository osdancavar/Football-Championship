    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if ((this.codResultado.getText().length() == 0)
                || (this.codJuego.getText().length() == 0)
                || (this.codEquipo.getText().length() == 0)
                || (this.textGol.getText().length() == 0)
                || (this.textEstado.getText().length() == 0)
                || (this.textPuntos.getText().length() == 0)) {
            Icon icon = null;
            JOptionPane.showMessageDialog(null, "ALGUN CAMPO VACIO", "ERROR", JOptionPane.DEFAULT_OPTION, icon);
        } else {
            int idResultado = parseInt(this.codResultado.getText());
            int idJuego = parseInt(this.codJuego.getText());
            int idEquipo = parseInt(this.codEquipo.getText());
            int Gol = parseInt(this.textGol.getText());
            String Estado = this.textEstado.getText();
            int Puntos = parseInt(this.textPuntos.getText());

            Icon icon = null;
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, icon);
            if (resp == JOptionPane.YES_OPTION) {
                ctrlResultado a = new ctrlResultado();
                a.insertarResultado(idResultado, idJuego, idEquipo, Gol, Estado, Puntos);
                this.textGol.setText(null);
                this.textEstado.setText(null);
                this.textPuntos.setText(null);
                this.codResultado.setText(null);
                this.codEquipo.setText(null);
                this.codJuego.setText(null);
                 agregar() ;
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

