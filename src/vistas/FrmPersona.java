    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if ((this.codDPI.getText().length() == 0)
                || (this.codEquipo.getText().length() == 0)
                || (this.codRol.getText().length() == 0)
                || (this.textFNaci.getText().length() == 0)
                || (this.textNombre.getText().length() == 0)
                || (this.textApellido.getText().length() == 0)) {
            Icon icon = null;
            JOptionPane.showMessageDialog(null, "ALGUN CAMPO VACIO", "ERROR", JOptionPane.DEFAULT_OPTION, icon);
        } else {
            String Nombre = this.textNombre.getText();
            String Apellido = this.textApellido.getText();
            String FechaNacimiento = this.textFNaci.getText();
            int DPI = parseInt(this.codDPI.getText());
            int idEquipo = parseInt(this.codEquipo.getText());
            int idRol = parseInt(this.codRol.getText());
            Icon icon = null;
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, icon);
            if (resp == JOptionPane.YES_OPTION) {
                ctrlPersona a = new ctrlPersona();
                a.insertarPersona(DPI, idEquipo, idRol, Nombre, Apellido, FechaNacimiento);
                this.textNombre.setText(null);
                this.textApellido.setText(null);
                this.textFNaci.setText(null);
                this.codDPI.setText(null);
                this.codEquipo.setText(null);
                this.codRol.setText(null);
                 agregar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

