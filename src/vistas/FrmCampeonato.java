    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if ((this.codCampeonato.getText().length() == 0)
                || (this.TextNombre.getText().length() == 0)
                || (this.txtFInicio.getText().length() == 0)
                || (this.txtFFinalizacion.getText().length() == 0)) {
            Icon icon = null;
            JOptionPane.showMessageDialog(null, "ALGUN CAMPO VACIO", "ERROR", JOptionPane.DEFAULT_OPTION, icon);
        } else {
            int idCampeonato = parseInt(this.codCampeonato.getText());
            String nombre = this.TextNombre.getText();
            String FechaInicio = this.txtFInicio.getText();
            String FechaFinalizacion = this.txtFFinalizacion.getText();
            Icon icon = null;
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, icon);
            if (resp == JOptionPane.YES_OPTION) {
                ctrlCampeonato a = new ctrlCampeonato();
                a.insertarCampeonato(idCampeonato, nombre, FechaInicio, FechaFinalizacion);
                this.TextNombre.setText(null);
                this.txtFInicio.setText(null);
                this.txtFFinalizacion.setText(null);
                this.codCampeonato.setText(null);
                agregar();
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

